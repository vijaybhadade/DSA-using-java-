
public class Solution {

    public static void main(String[] args) {
        System.out.println("Bit wise AND =" + (5 & 6));
        System.out.println("Bit wise OR =" + (5 | 6));
        System.out.println("Bit wise XOR =" + (5 ^ 6));
        System.out.println("Bit wise one's complement =" + ~3);
        System.out.println("Bit wise left shift =" + (5 << 6));
        System.out.println("Bit wise right shift =" + (6 >> 5));
    }
}
